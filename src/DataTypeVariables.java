public class DataTypeVariables {
    public static void main(String[] args)
    {

        boolean isABoolean = true;
        char singleChar = 'A';
        byte byteInteger = 127;
        short shortInteger = 32767;
        int intInteger = 2146483647;
        long longInteger = 15000000000L;
        float floatNumber = 6.85f;
        double doubleFloatNumber = 19.69;

        System.out.println("isABoolean: " + isABoolean);
        System.out.println("singleChar: " + singleChar);
        System.out.println("byteInteger: " + byteInteger);
        System.out.println("shortInteger: " + shortInteger);
        System.out.println("intInteger: " + intInteger);
        System.out.println("longInteger: " + longInteger);
        System.out.println("floatNumber: " + floatNumber);
        System.out.println("doubleFloatNumber: " + doubleFloatNumber);
    }
}
